// Helper functions for bot to get campaign insights
export function getCampaignSummary() {
  const totalCampaigns = 30;
  const totalSpend = mockMetrics.totalSpend;
  const totalRevenue = mockMetrics.totalRevenue;
  const averageRoas = mockMetrics.averageRoas;

  const googleCampaigns = 16;
  const metaCampaigns = 14;

  const bestCampaign = mockCampaigns.find((c) => c.id === "14");
  const worstCampaign = mockCampaigns.find((c) => c.id === "16");

  return {
    totalCampaigns,
    totalSpend,
    totalRevenue,
    averageRoas,
    googleCampaigns,
    metaCampaigns,
    bestCampaign,
    worstCampaign,
    campaigns: mockCampaigns,
  };
}

export function getTopPerformingCampaigns(limit = 3) {
  return [
    mockCampaigns.find((c) => c.id === "14"), // ROAS 4.75
    mockCampaigns.find((c) => c.id === "1"), // ROAS 2.21
  ]
    .filter(Boolean)
    .slice(0, limit);
}

export function getWorstPerformingCampaigns(limit = 3) {
  return [
    mockCampaigns.find((c) => c.id === "16"), // ROAS 2.19 (worst)
    mockCampaigns.find((c) => c.id === "1"), // ROAS 2.21
  ]
    .filter(Boolean)
    .slice(0, limit);
}

export function getCampaignsByPlatform(platform: "Google Ads" | "Meta Ads") {
  return mockCampaigns.filter((c) => c.platform_name === platform);
}
